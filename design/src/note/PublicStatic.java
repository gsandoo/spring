package note;

public class PublicStatic {

//    수식자 :  글래스나 메서드 , 필드 등에 대해 효과나 제약을 지정

//    접근제어자: 어떤 범위에서 접근을 허가할지를 지정

//    접근을 허용하는 정도: public >  protected > default >  private

//    public :  접근제한x

//    protected: 동일한 패키지 , 상속받은 하위클래스 사용 가능
//    변수가 protected 인 클래스를 import 받으면 패키지가 달라도 사용 가능

//    default :  동일한 패키지 내에서만 접근 가능.

//    private : 자기자신 클래스 내에서만 접근이 가능.
//    client 가 직접 변수에 접근하지 못하게 막아야함. 사용자는 메서드만 사용한다.


//    static: 공유의 의미를 담은 키워드.
//    static 변수는 인스턴스를 생성하지 않고 직접 값을 지정해 사용할 수 있다.
//    주로 public 과 함께쓰인다. 전역변수이기 때문. 여러곳에서 기준값 처럼 쓰임.
}
